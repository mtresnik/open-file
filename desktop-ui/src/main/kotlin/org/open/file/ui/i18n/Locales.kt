/**
 * Built-in locale registry. Shipping more means: define a new [Strings]
 * instance below and add it to [locales].
 *
 * Each locale carries a BCP-47 code (`"en"`, `"es"`), a display name the
 * language picker renders, and a flag emoji — the language dialog uses the
 * flag as a quick visual cue and falls back to the code if your terminal
 * doesn't render flag emoji.
 */
data class Locale(
    /** BCP-47 tag; used as the persisted key and for Java locale interop. */
    val code: String,
    val displayName: String,
    val flag: String,
    val strings: Strings,
)

